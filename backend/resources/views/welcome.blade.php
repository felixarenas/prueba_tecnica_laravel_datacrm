                                                        t&eacute;cnicas necesarias para conseguir cerrar una venta,
                                                        as&iacute; como las acciones que potencien la
                                                        fidelizaci&oacute;n de clientes.</span></p>

                                                <p>&nbsp;</p>

                                                <p><span style="font-size:18px">En esta etapa, es importante ejecutar un
                                                        seguimiento de todo el proceso con el fin de determinar si se
                                                        est&aacute; haciendo lo correcto para alcanzar los objetivos, si
                                                        nuestras acciones son eficaces. </span></p>

