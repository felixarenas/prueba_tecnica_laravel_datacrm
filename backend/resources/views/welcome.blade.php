                                                        si cada miembro, adem&aacute;s de estar capacitado para realizar
                                                        las tareas asignadas, est&aacute; motivado. </span></p>

                                                <p>&nbsp;</p>

                                                <p><span style="font-size:18px">Al mismo tiempo, el gerente debe
